package org.runbuddy.tomahawk.views;

import android.content.Context;
import android.support.v4.view.ViewPager;
import android.util.AttributeSet;
import android.view.GestureDetector;
import android.view.MotionEvent;

public class AlbumArtViewPager extends ViewPager {

    private GestureDetector mGestureDetector;

    public AlbumArtViewPager(Context context) {
        super(context);
    }

    public AlbumArtViewPager(Context context, AttributeSet attrs) {
        super(context, attrs);
    }

    @Override
    public boolean onInterceptTouchEvent(MotionEvent event) {
        return true;
    }

    @Override
    public boolean onTouchEvent(MotionEvent event) {
        if (mGestureDetector != null) {
            mGestureDetector.onTouchEvent(event);
        }
        super.onTouchEvent(event);

        return true;
    }

    public void setOnGestureListener(GestureDetector.SimpleOnGestureListener listener) {
        mGestureDetector = new GestureDetector(getContext(), listener);
    }
}
